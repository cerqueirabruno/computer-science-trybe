# OPERADORES DE COMPARAÇÃO;

# == : igualdade;
# != : diferente;
# > : maior;
# >= : maior ou igual;
# < : menor;
# <= : menor ou igual;

a = 1
b = 2
c = "1"

print(a == b)  # False
print(a != b)  # True
print(a > b)  # False
print(a >= b)  # False
print(a < b)  # True
print(a <= b)  # True

print(a == c)  # False
print(a != c)  # True
