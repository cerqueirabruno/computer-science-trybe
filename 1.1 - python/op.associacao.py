# in: retorna verdadeiro se encontrar um item específico em uma sequência;
# not in: retorna verdadeiro se não encontrar um item
#   específico em uma sequência;

lista = [1, 2, 3, 4, 5]

print(3 in lista)  # True
print(6 in lista)  # False

print(3 not in lista)  # False
print(6 not in lista)  # True
